package com.jakutenshi.rmcomix.controllers;

import javafx.animation.KeyFrame;
import javafx.animation.KeyValue;
import javafx.animation.Timeline;
import javafx.beans.property.DoubleProperty;
import javafx.event.ActionEvent;
import javafx.event.EventHandler;
import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.layout.StackPane;
import javafx.util.Duration;

import java.util.HashMap;

public class ScreensController extends StackPane {

    public final static String  COMICS_VIEW_SCREEN_ID = "ComicsView";
    public final static String  COMICS_VIEW_SCREEN_FXML = "/com/jakutenshi/rmcomix/view/comics/comics_view.fxml";
    public final static String  SETTINGS_VIEW_SCREEN_ID = "SettingsView";
    public final static String  SETTINGS_VIEW_SCREEN_FXML = "/com/jakutenshi/rmcomix/view/settings/comics_settings.fxml";

    private HashMap<String, Parent> screens = new HashMap<String, Parent>();
    private HashMap<String, FXMLLoader> screensLoaders = new HashMap<String, FXMLLoader>(); /* таблица с лоадерами,
     чтобы в контроллерах можно было вызывать методы других контроллеров */
    
    public ScreensController() {
        super();
    }

    public void addScreen(String name, Parent screen) {
        screens.put(name, screen);
    }

    public Parent getScreen(String name) {
        return screens.get(name);
    }

    public FXMLLoader getLoaders(String name) {
        return screensLoaders.get(name);
    }

    public boolean loadScreen(String name, String resource) {
        try {
            FXMLLoader myLoader = new FXMLLoader(getClass().getResource(resource));
            Parent loadScreen = (Parent) myLoader.load();
            ControlledScreen myScreenControler = ((ControlledScreen) myLoader.getController());
            myScreenControler.setScreenParent(this);
            addScreen(name, loadScreen);
            screensLoaders.put(name, myLoader);
            return true;
        } catch (Exception e) {
            System.out.println(e.getMessage());
            return false;
        }
    }

    public boolean setScreen(final String name) {
        if (screens.get(name) != null) {   //screen loaded
            final DoubleProperty opacity = opacityProperty();

            if (!getChildren().isEmpty()) {
                Timeline fade = new Timeline(
                        new KeyFrame(Duration.ZERO, new KeyValue(opacity, 1.0)),
                        new KeyFrame(new Duration(500), new EventHandler<ActionEvent>() {
                    @Override
                    public void handle(ActionEvent t) {
                        getChildren().remove(0);                    //remove the displayed screen
                        getChildren().add(0, screens.get(name));     //add the screen
                        Timeline fadeIn = new Timeline(
                                new KeyFrame(Duration.ZERO, new KeyValue(opacity, 0.0)),
                                new KeyFrame(new Duration(500), new KeyValue(opacity, 1.0)));
                        fadeIn.play();
                    }
                }, new KeyValue(opacity, 0.0)));
                fade.play();

            } else {
                setOpacity(0.0);
                getChildren().add(screens.get(name));
                Timeline fadeIn = new Timeline(
                        new KeyFrame(Duration.ZERO, new KeyValue(opacity, 0.0)),
                        new KeyFrame(new Duration(500), new KeyValue(opacity, 1.0)));
                fadeIn.play();
            }
            return true;
        } else {
            System.out.println("screen hasn't been loaded!!! \n");
            return false;
        }
    }
}

